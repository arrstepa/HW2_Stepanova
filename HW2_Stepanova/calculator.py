def multiple(a,b):
	"Функция умножения двух чисел"
	return a*b

def substraction(a, b):
	"Функция вычитания двух чисел"
	return a - b

def addition (a, b):
    "сложение двух чисел"
    return a + b

def division (a, b):
    "деление двух чисел"
    if b!= 0:
        return a/b
    else:
        return "деление на 0!"
