def multiple(a,b):
	"Функция умножения двух чисел"
	return a*b

def substraction(a, b):
	"Функция вычитания b из a"
	return a - b

def addition (a, b):
	"Функция сложения двух чисел"
	return a + b

def division (a, b):
	"Функция деления a на b"
	if b!= 0:
		return a/b
	else:
		return "деление на 0!"
